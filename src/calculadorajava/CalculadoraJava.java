    private void btnIgualMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIgualMouseClicked
        String texto = txtfPantalla.getText();
        String[] parts;
        switch (this.signo) {
            case '+':
                parts = texto.split("\\+");
                this.numero2 = Float.parseFloat(parts[1]);
                this.guardar = operacion.sumar(this.numero1, this.numero2);
                txtfPantalla.setText(String.valueOf(guardar));
                break;
            case '-':
                parts = texto.split("-");
                this.numero2 = Float.parseFloat(parts[1]);
                 this.guardar = operacion.restar(this.numero1, this.numero2);
                txtfPantalla.setText(String.valueOf(guardar));
                break;
            case '*':
                parts = texto.split("x");
                this.numero2 = Float.parseFloat(parts[1]);
                 this.guardar = operacion.multiplicar(this.numero1, this.numero2);
                txtfPantalla.setText(String.valueOf(guardar));
                break;
            case 'รท':
                parts = texto.split("รท");
                this.numero2 = Float.parseFloat(parts[1]);
                 this.guardar = operacion.dividir(this.numero1, this.numero2);
                txtfPantalla.setText(String.valueOf(guardar));
                break;
                 case '%':
                parts = texto.split("%");
                this.numero2 = Float.parseFloat(parts[1]);
                 this.guardar = operacion.porcentaje(this.numero1, this.numero2);
                txtfPantalla.setText(String.valueOf(guardar));
                break;

        }


    }//GEN-LAST:event_btnIgualMouseClicked

